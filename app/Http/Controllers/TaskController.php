     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $validate = '';
        $activity = '';
        $validate = Task::where('name',$request->name)->where('location_id',$request->location_id)->get();

        if(!sizeof($validate)){
            $activity = Task::create(['name' => $request->name,'location_id' => $request->location_id,'position' => $request->position,'duration' => $request->duration]);
            $location = Location::find($request->location_id);
            $activity->location = $location;

            return response()->json([
                'activity' => $activity
            ]);
        }
    }

